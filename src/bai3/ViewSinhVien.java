    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String tam = JOptionPane.showInputDialog(this, "Bạn nhập kiẻu cần sắp xếp(Điển/Tên)");
//        System.out.println(tam);
        if (tam.equalsIgnoreCase("Điển")) {
            service.listSinhVien.sort(new Comparator<SinhVien>() {
                @Override
                public int compare(SinhVien o1, SinhVien o2) {
                    return Double.compare(o1.getDiem(), o2.getDiem());
                }
            });
            showAll();
        } else {
            service.getAll().sort(new Comparator<SinhVien>() {
                @Override
                public int compare(SinhVien o1, SinhVien o2) {
                    return o1.getTen().compareToIgnoreCase(o2.getTen());
                }
            });
            showAll();
        }


    }//GEN-LAST:event_jButton2ActionPerformed

