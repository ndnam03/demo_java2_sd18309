    public void showAll() {
        tm.setRowCount(0);
        for (SinhVien o : service.getAll()) {
            tm.addRow((Object[]) o.toObject());
        }
    }

    public SinhVien fillData() {

        String ten = txtName.getText().trim();
        double diem = 0;
        String nghanh = (String) cbNghanh.getSelectedItem();
        if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống!!");
                return null;
        } else {
            if (ten.matches("[a-zA-Z ]+ ")) {
                JOptionPane.showMessageDialog(this, "Không đúng mẫu");
                return null;
            }
        }
        try {
            diem = Double.parseDouble(txtDiem.getText());
            if(diem < 0 || diem > 10){
                JOptionPane.showMessageDialog(this, "Dêirm k dung");
                return null;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Diem k phải là so");
            
        }
        return new SinhVien(ten, diem, nghanh);
    }

