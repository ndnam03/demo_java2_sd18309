    public void fillValueForm(int row) {
        txtName.setText((String) tm.getValueAt(row, 0));

        String diem = String.valueOf(tm.getValueAt(row, 1));
        txtDiem.setText(diem);

        cbNghanh.setSelectedItem((String) tm.getValueAt(row, 2));
        txtHocluc.setText((String) tm.getValueAt(row, 3));
        if (tm.getValueAt(row, 4).equals("Có")) {
            ckThuong.setSelected(true);
        } else {
            ckThuong.setSelected(false);
        }

    }
